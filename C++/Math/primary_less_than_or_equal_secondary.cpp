#include <iostream>

// Check if the primary value is less than or equal to the secondary value.
bool primaryLessThanOrEqualToSecondary(int primary, int secondary) {
    return primary <= secondary;
}


int main() {
    // Check if the primary value is less than or equal to the secondary value.
    printf("%d \n", primaryLessThanOrEqualToSecondary(1, 10));
    return 0;
}